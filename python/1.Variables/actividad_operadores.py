

#ACTIVIDAD 1

first_num = int(input("Ingresa tu primer numero: "))
second_num = int(input("Ingresa tu segundo numero: "))

#Operaciones aritmeticas

print(f'La suma es: {first_num + second_num}')
print(f'La multiplicacion es: {first_num * second_num}')

#Operaciones de comparacion

print(f'Los numeros son iguales? {first_num == second_num}')
print(f'El primer numero es menor que el segundo? {first_num < second_num}')
print(f'El primer numero es mayor que el segundo? {first_num > second_num}')
